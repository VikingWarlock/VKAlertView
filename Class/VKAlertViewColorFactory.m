#import "VKAlertViewColorFactory.h"

@implementation VKAlertViewColorFactory

+(UIColor *)VKRedColor
{
    return [UIColor redColor];
}

+(UIColor *)VKBlueColor
{
    return [UIColor blueColor];
}

+(UIColor *)VKSystemBlue
{
    return [UIColor blueColor];
}

+(UIColor *)VKBackgroundColor
{
    return [UIColor colorWithRed:250.f/255.f green:250.f/255.f blue:250.f/255.f alpha:1.f];
}

+(UIColor *)VKGreenColor
{
    return [UIColor greenColor];
}

@end
